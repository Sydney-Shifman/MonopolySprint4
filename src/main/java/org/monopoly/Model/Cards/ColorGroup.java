package org.monopoly.Model.Cards;

/**
 * Enum representing the color groups of properties in Monopoly.
 * Each color group corresponds to a set of properties on the game board.
 *
 * Developed by: shifmans
 */
public enum ColorGroup {
    BROWN,
    LIGHT_BLUE,
    PINK,
    ORANGE,
    RED,
    YELLOW,
    GREEN,
    DARK_BLUE,
    RAILROAD,
    UTILITY
}
